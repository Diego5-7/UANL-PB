num1 = float(input("Ingresa el primer número, no es valido 0: "))
num2 = float(input("Ingresa el segundo número, no es valido 0: "))

suma = num1 + num2
print(f"Suma: {suma}")

resta = num1 - num2
print(f"Resta: {resta}")

multiplicacion = num1 * num2
print(f"Multiplicación: {multiplicacion}")

division = num1 / num2
print(f"División: {division}")

potenciacion = num1 ** num2
print(f"Potenciación: {potenciacion}")

import math
raiz_cuadrada = math.sqrt(num1)
print(f"Raíz cuadrada de {num1}: {raiz_cuadrada}")

modulo = num1 % num2
print(f"Módulo: {modulo}")